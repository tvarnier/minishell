#include "minishell.h"

int		b_unsetenv(t_shell *s, char **av, int ac)
{
	int		i;

	if (ac >= 2)
	{
		i = 0;
		while (++i < ac)
			unset_env(&(s->env), av[i]);
	}
	return (0);
}
