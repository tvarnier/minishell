#include "libft.h"

int		ft_intlen(int n)
{
	if (n == -2147483648)
		return (11);
	if (n < 0)
		return (ft_intlen(n * (-1)) + 1);
	if (n > 9)
		return (ft_intlen(n / 10) + 1);
	return (1);
}
